package fmcg.master.setup.data;

public class Menu extends CommonData {
	private String menuId;
	private String menuName;
	private String descrition;
	private String shortName;
	private String requiredInLandingScreen;
	private String status;
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getDescrition() {
		return descrition;
	}
	public void setDescrition(String descrition) {
		this.descrition = descrition;
	}
	public String getShortName() {
		return shortName;
	}
	public void setShortName(String shortName) {
		this.shortName = shortName;
	}
	public String getRequiredInLandingScreen() {
		return requiredInLandingScreen;
	}
	public void setRequiredInLandingScreen(String requiredInLandingScreen) {
		this.requiredInLandingScreen = requiredInLandingScreen;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	

}
